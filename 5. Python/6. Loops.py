# using for loop.
for i in [0,1,2,3,4,5]:
    print(i+1)

# ranging the numbers:- range(10) and range(1, 10) => range(min, max)
for i in range(4, 10):
    print(i)

# printing the names in the loop:-
names = ["Harry", "Ron", "Hermoine", "Ginny", "Draco"]

for name in names:
    print(name)

# printing a character from the name:-
name = "javascript"
for char in name:
    print(char)